def parse_extra_word_bank() -> list[dict]:
    items: list[dict] = []
    for line in RAW_EXTRA_WORDS.splitlines():
        line = line.strip()
        if not line:
            continue
        english, japanese = line.split('|', 1)
        items.append(
            {
                'english': english.strip(),
                'japanese': [part.strip() for part in japanese.split('/') if part.strip()],
            }
        )
    return items


EXTRA_WORD_BANK = parse_extra_word_bank()
